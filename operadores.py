x=10
y=5

# igual a
print(f'-{x==y}') 

# diferente de
print(f'-{x!=y}') 

# maior que
print(f'-{x>y}') 

# menor que
print(f'-{x<y}')

# maior ou igual
print(f'-{x>=y}')

# menor ou igual 
print(f'-{x<=y}')

# and
print(x<=10 and y>=5)
print(x>10 and y<5)

# or
print(x<=10 or y<=5)
print(x>10 or y>5)

# not
print(not(y<10))
print(not(x>10))

